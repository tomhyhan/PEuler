import numpy as np
import math

def solution():
    seen = set()
    cnt = 0
    f_memo = {}
    for n in range(1, 1000000):
        visited = set()
        df = n
        while df not in visited:
            visited.add(df)
            df = factorial_sum(df, f_memo)
        if len(visited) == 60:
            cnt += 1
    print(cnt)
        
def factorial_sum(num, f_memo):
    if num in f_memo:
        return f_memo[num]
    f_sum = sum([math.factorial(int(i)) for i in str(num)])
    f_memo[num] = f_sum
    return f_memo[num]
